public class incremento {
    public static void main(String[] args){

        //incremento
        int numero = 5;

        numero++; //É a mesma coisa que numero + 1;

        System.out.println(numero);//Resultado = 6

        //decremento

        numero--;// É a mesma coisa que numero -1;
        System.out.println(numero);


    }
}
